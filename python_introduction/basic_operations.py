#This is the addition class of two variables
class addition:
    number1 = 10
    number2 = 5
    
    answer = number1 + number2
    print("Addition of", number1, "and ", number2, "=", answer)

#This is the subtraction class of two variables   
class subtraction:
    number1 = 10
    number2 = 5
    
    answer = number1 - number2
    print("Subtraction 0f", number1, "and", number2, "=", answer)

#This is the multiplication class of two variables
class multiplication:
    number1 = 10
    number2 = 5
    
    answer = number1 * number2
    print("Multiplication of", number1, "and", number2, "=", answer)
    
